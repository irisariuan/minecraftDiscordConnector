/** Find JARs in a directory whose name matches a prefix, e.g. "paper-" */
export async function findJarsByPrefix(
	dir: string,
	prefix: string,
): Promise<string[]> {
	if (!existsSync(dir)) return [];
	const files = await readdir(dir);
	return files.filter((f) => f.startsWith(prefix) && f.endsWith(".jar"));
}

/** Run the Forge installer in installServer mode, returns success + output */
export async function runForgeInstaller(
	installerPath: string,
	serverDir: string,
): Promise<{ ok: boolean; output: string }> {
	const proc = Bun.spawn(["java", "-jar", installerPath, "--installServer"], {
		cwd: serverDir,
		stdout: "pipe",
		stderr: "pipe",
	});
	await proc.exited;
	const stdout = await new Response(proc.stdout).text();
	const stderr = await new Response(proc.stderr).text();
	return {
		ok: proc.exitCode === 0,
		output: [stdout, stderr].filter(Boolean).join("\n").slice(0, 1500),
	};
}

// ─── Compatibility check ──────────────────────────────────────────────────────

export async function checkPluginCompatibility(
	serverId: number,
	newMcVersion: string,
	loaderType: string,
): Promise<CompatResult[]> {
	const dbPlugins = await getPluginsByServerId(serverId);
	if (dbPlugins.length === 0) return [];

	const results = await Promise.all(
		dbPlugins.map(async (p): Promise<CompatResult> => {
			const projectInfo = await getPlugin(p.projectId).catch(() => null);
			const title = projectInfo?.title ?? p.projectId;

			const versions = await listPluginVersions(p.projectId, {
				game_versions: [newMcVersion],
				loaders: [loaderType],
			}).catch(() => null);

			if (!versions) {
				return {
					projectId: p.projectId,
					title,
					currentVersion: p.versionId,
					compatible: null,
					availableVersionId: null,
				};
			}

			const newest =
				versions.sort(
					(a, b) => b.date_published - a.date_published,
				)[0] ?? null;

			return {
				projectId: p.projectId,
				title,
				currentVersion: p.versionId,
				compatible: versions.length > 0,
				availableVersionId: newest?.id ?? null,
			};
		}),
	);

	return results;
}

/**
 * Check all DB-tracked plugins for this server against Modrinth and return
 * entries where a newer version is available for the server's current
 * Minecraft version + loader.
 */
export async function checkOutdatedPlugins(server: Server): Promise<{
	outdated: RichUpdateEntry[];
	failed: string[];
}> {
	const plugins = await getPluginsByServerId(server.id);
	if (plugins.length === 0) return { outdated: [], failed: [] };

	// Keep only the most-recently updated record per projectId
	const latestByProject = new Map<string, DbPlugin>();
	for (const plugin of plugins) {
		const existing = latestByProject.get(plugin.projectId);
		if (!existing || plugin.updatedAt > existing.updatedAt) {
			latestByProject.set(plugin.projectId, plugin);
		}
	}

	const outdated: RichUpdateEntry[] = [];
	const failed: string[] = [];

	for (const [projectId, plugin] of latestByProject.entries()) {
		const [versions, currentVersionDetails, projectDetails] =
			await Promise.all([
				listPluginVersions(projectId, {
					loaders: [server.config.loaderType],
					game_versions: [server.config.minecraftVersion],
				}),
				getPluginVersionDetails(plugin.versionId),
				getPlugin(projectId),
			]);

		if (!versions || versions.length === 0) {
			failed.push(projectDetails?.title ?? projectId);
			continue;
		}

		const latest = versions[0];
		if (!latest) {
			failed.push(projectDetails?.title ?? projectId);
			continue;
		}

		// Already up-to-date — skip silently
		if (latest.id === plugin.versionId) continue;

		outdated.push({
			plugin,
			projectTitle: projectDetails?.title ?? projectId,
			currentVersionNumber:
				currentVersionDetails?.version_number ?? plugin.versionId,
			currentVersionDate: currentVersionDetails?.date_published
				? new Date(currentVersionDetails.date_published).getTime()
				: null,
			newVersionId: latest.id,
			newVersionNumber: latest.version_number,
			newVersionDate: latest.date_published,
			newFilename: latest.files[0]?.filename ?? "unknown",
			newFileSize: latest.files[0]?.size ?? null,
		});
	}

	return { outdated, failed };
}

/**
 * Build a compact embed summarising outdated plugins found by
 * {@link checkOutdatedPlugins}.  Returns `null` when everything is up-to-date
 * and there is nothing to show.
 */
export function buildOutdatedPluginsEmbed(
	outdated: RichUpdateEntry[],
	failed: string[],
	serverTag: string,
): EmbedBuilder | null {
	if (outdated.length === 0 && failed.length === 0) return null;

	const embed = new EmbedBuilder()
		.setTitle("🔌 Outdated Plugin Check")
		.setColor(outdated.length > 0 ? 0xf39c12 : 0x95a5a6)
		.setDescription(
			outdated.length > 0
				? `${bold(serverTag)} has ${bold(String(outdated.length))} outdated plugin${outdated.length !== 1 ? "s" : ""}. Run ${inlineCode("/updateplugins")} to update.`
				: `${bold(serverTag)} — all tracked plugins are up to date.`,
		)
		.setTimestamp();

	if (outdated.length > 0) {
		const MAX_SHOWN = 10;
		const lines = outdated
			.slice(0, MAX_SHOWN)
			.map(
				(r) =>
					`• ${bold(r.projectTitle)} — ${inlineCode(r.currentVersionNumber)} → ${inlineCode(r.newVersionNumber)}`,
			)
			.join("\n");
		const overflow =
			outdated.length > MAX_SHOWN
				? `\n${italic(`…and ${outdated.length - MAX_SHOWN} more`)}`
				: "";
		embed.addFields({
			name: `⬆️ Updates Available (${outdated.length})`,
			value: trimTextWithSuffix(lines + overflow, 1024),
		});
	}

	if (failed.length > 0) {
		embed.addFields({
			name: `❓ Could Not Check (${failed.length})`,
			value: trimTextWithSuffix(
				failed.map((id) => inlineCode(id)).join(", "),
				1024,
			),
		});
	}

	return embed;
}

export function buildCompatEmbed(
	results: CompatResult[],
	serverTag: string,
	newVersion: string,
): EmbedBuilder {
	const compatible = results.filter((r) => r.compatible === true);
	const incompatible = results.filter((r) => r.compatible === false);
	const unknown = results.filter((r) => r.compatible === null);

	const formatList = (items: CompatResult[]) =>
		items.length === 0
			? italic("None")
			: items
					.map(
						(r) =>
							`• ${bold(r.title)} (${inlineCode(r.projectId)})`,
					)
					.join("\n");

	const embed = new EmbedBuilder()
		.setTitle(`🔍 Compatibility check — upgrading to ${newVersion}`)
		.setDescription(
			`Server: ${bold(serverTag)}\n\nChecked ${bold(String(results.length))} installed plugin(s)/mod(s) against Minecraft ${bold(newVersion)}.`,
		)
		.setColor(
			incompatible.length > 0
				? 0xe74c3c
				: unknown.length > 0
					? 0xf39c12
					: 0x2ecc71,
		)
		.setTimestamp();

	if (compatible.length > 0) {
		embed.addFields({
			name: `✅ Compatible (${compatible.length})`,
			value: trimTextWithSuffix(formatList(compatible), 1024),
		});
	}
	if (incompatible.length > 0) {
		embed.addFields({
			name: `❌ Incompatible (${incompatible.length})`,
			value: trimTextWithSuffix(formatList(incompatible), 1024),
		});
	}
	if (unknown.length > 0) {
		embed.addFields({
			name: `❓ Unknown / not on Modrinth (${unknown.length})`,
			value: trimTextWithSuffix(formatList(unknown), 1024),
		});
	}

	return embed;
}

function buildFacets(
	facets?: Partial<SearchPluginFacets>,
	skipServerSideFilter = false,
) {
	const result: string[][] = [];
	if (facets?.categories) {
		result.push([...facets.categories.map((v) => `categories:${v}`)]);
	}
	if (facets?.versions) {
		result.push([...facets.versions.map((v) => `versions:${v}`)]);
	}
	if (facets?.project_type) {
		result.push([...facets.project_type.map((v) => `project_type:${v}`)]);
	}

	if (!skipServerSideFilter) {
		result.push([
			`server_side:${SideValue.Optional}`,
			`server_side:${SideValue.Required}`,
		]);
	}
	return result;
}

export async function searchPlugins({
	offset = 0,
	query,
	facets,
	skipServerSideFilter = false,
}: SearchPluginProps) {
	const url = new URL(`${MODRINTH_API_BASE}/search`);
	url.searchParams.set("limit", "100");
	url.searchParams.set("offset", offset.toString());
	url.searchParams.set(
		"facets",
		JSON.stringify(buildFacets(facets, skipServerSideFilter)),
	);

	if (query) {
		url.searchParams.set("query", query);
	}
	const res = await safeFetch(url);
	const data = (await res?.json().catch<ErrorResponse>((err) => ({
		error: "[CLIENT] failed to parse JSON",
		description: err.message,
	}))) as PluginSearchQueryResponse | ErrorResponse;
	return data;
}

