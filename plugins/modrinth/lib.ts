// ─── Vanilla helpers ──────────────────────────────────────────────────────────

export async function getMojangReleaseVersions(): Promise<string[]> {
	const res = await fetch(MOJANG_MANIFEST_URL).catch(() => null);
	if (!res?.ok) return [];
	const data = (await res.json()) as MojangManifest;
	return data.versions.filter((v) => v.type === "release").map((v) => v.id);
}

export async function getVanillaServerUrl(
	version: string,
): Promise<string | null> {
	const res = await fetch(MOJANG_MANIFEST_URL).catch(() => null);
	if (!res?.ok) return null;
	const manifest = (await res.json()) as MojangManifest;
	const entry = manifest.versions.find((v) => v.id === version);
	if (!entry) return null;
	const infoRes = await fetch(entry.url).catch(() => null);
	if (!infoRes?.ok) return null;
	const info = (await infoRes.json()) as MojangVersionInfo;
	return info.downloads.server?.url ?? null;
}

